`timescale 1ns / 1ps
module splash_screen_player1 (
    input  wire [6:0] x,   // OLED X coordinate (0-95)
    input  wire [6:0] y,   // OLED Y coordinate (0-63)
    output reg  [15:0] pixel_out
);

    reg [95:0] splash_bitmap [0:13];

    initial begin
        splash_bitmap[0]  = 96'b000000000000000000011110010000000100010001011111011110000000000100000110001110000000000000000000; // y = 19
        splash_bitmap[1]  = 96'b000000000000000000010001010000001010010001010000010001000000001100000110010001000000000000000000; // y = 20
        splash_bitmap[2]  = 96'b000000000000000000010001010000010001001010010000010001000000000100000100010000000000000000000000; // y = 21
        splash_bitmap[3]  = 96'b000000000000000000011110010000010001000100011110011110000000000100001000001110000000000000000000; // y = 22
        splash_bitmap[4]  = 96'b000000000000000000010000010000011111000100010000010100000000000100000000000001000000000000000000; // y = 23
        splash_bitmap[5]  = 96'b000000000000000000010000010000010001000100010000010010000000000100000000010001000000000000000000; // y = 24
        splash_bitmap[6]  = 96'b000000000000000000010000011111010001000100011111010001000000001110000000001110000000000000000000; // y = 25
        splash_bitmap[7]  = 96'b000000000000000000000000000000000000111110100010111100100010000000000000000000000000000000000000; // y = 33
        splash_bitmap[8]  = 96'b000000000000000000000000000000000000101010100010100010100010000000000000000000000000000000000000; // y = 34
        splash_bitmap[9]  = 96'b000000000000000000000000000000000000001000100010100010110010000000000000000000000000000000000000; // y = 35
        splash_bitmap[10] = 96'b000000000000000000000000000000000000001000100010111100101010000000000000000000000000000000000000; // y = 36
        splash_bitmap[11] = 96'b000000000000000000000000000000000000001000100010101000100110000000000000000000000000000000000000; // y = 37
        splash_bitmap[12] = 96'b000000000000000000000000000000000000001000100010100100100010000000000000000000000000000000000000; // y = 38
        splash_bitmap[13] = 96'b000000000000000000000000000000000000001000111110100010100010000000000000000000000000000000000000; // y = 39
    end

    always @(*) begin
        case (y)
            6'd19: pixel_out = splash_bitmap[0][95 - x] ? 16'hFFFF : 16'h0000;
            6'd20: pixel_out = splash_bitmap[1][95 - x] ? 16'hFFFF : 16'h0000;
            6'd21: pixel_out = splash_bitmap[2][95 - x] ? 16'hFFFF : 16'h0000;
            6'd22: pixel_out = splash_bitmap[3][95 - x] ? 16'hFFFF : 16'h0000;
            6'd23: pixel_out = splash_bitmap[4][95 - x] ? 16'hFFFF : 16'h0000;
            6'd24: pixel_out = splash_bitmap[5][95 - x] ? 16'hFFFF : 16'h0000;
            6'd25: pixel_out = splash_bitmap[6][95 - x] ? 16'hFFFF : 16'h0000;
            6'd33: pixel_out = splash_bitmap[7][95 - x] ? 16'hFFFF : 16'h0000;
            6'd34: pixel_out = splash_bitmap[8][95 - x] ? 16'hFFFF : 16'h0000;
            6'd35: pixel_out = splash_bitmap[9][95 - x] ? 16'hFFFF : 16'h0000;
            6'd36: pixel_out = splash_bitmap[10][95 - x] ? 16'hFFFF : 16'h0000;
            6'd37: pixel_out = splash_bitmap[11][95 - x] ? 16'hFFFF : 16'h0000;
            6'd38: pixel_out = splash_bitmap[12][95 - x] ? 16'hFFFF : 16'h0000;
            6'd39: pixel_out = splash_bitmap[13][95 - x] ? 16'hFFFF : 16'h0000;
            default: pixel_out = 16'h0000;
        endcase
    end

endmodule